# 6. EJEMPLO DE IMPLEMENTACIÓN REAL
# ==============================================================================
# Estos 9 valores son los que extraes en tiempo real con tu script de Mediapipe
ejemplo_input = [175.0, 174.0, 178.0, 177.0, 89.0, 90.0, 5.0, 0.35, 0.40]

resultado = predecir_calidad_rf(ejemplo_input)
